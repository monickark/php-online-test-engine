                  <p>Impetus TechKnows has developed its Advanced TAMS (Tests & Assessment Management System)software engine basis a framework that maps current industry requirements of various competencies and employability skills for a ‘right fitment’ in every type of assessee.  Our framework provides the scope of assessing an individual for various skill sets that help in the ‘Person-Job’ Fitment.  This is very important for any company and forms an important stage of the selection process. <br> 
<p>The framework is based on the following key areas of skills to be identified that are applicable to different verticals and levels of employment.<br>
1. Technical Skills (To assess key skills that are required for a particular job - basis job requirement, definition, scope and defined protocols)<br>
2. Functional Skills (To assess key skills that are required in any job - Plan, Organize, Communicate, Manage, Report, Innovate, Effective understanding of tools, equipment’s, infrastructure and purpose, regulations, other functional role competencies for diverse perspectives and desired outcomes)<br>
3. Soft Skills (To assess key skills for ‘person-company’ fitment, manage work-life balance, digital literacy, work place ethics, team work, adaptability, ownership, attitude and interest)<br>
<p>The A-ONE framework focuses in identifying the above skills during the pre-hire or hire stages of employment so that the identification of the ‘right’ work-force is achieved at a reduced cost for the HR of any company through our assessment model.  It also provides scope for job seekers with an alternative, innovative and inexpensive model to prepare themselves for entering into a new employment or a career shift.
                 
<p> As an advantage to the candidate and the employers, the test takers are able to choose their appropriate tests basis their current profile and requirement.
<p>This is highly important because there is no such thing as a perfect employee.  Every individual is an asset in accordance the type of skills and application areas.  It becomes necessary as an Assessment company for us to ensure that assessments are done for different verticals and candidate profiles so that an accurate mapping of core competencies can be identified.
Some of the current types available in our ONLINE & OFFLINE Assessment capabilities are;
•	JIT – Job Interview Testing (Pre Hire)
•	GET – Graduate Employability Testing (Engineering, Technology, Arts & Sciences & Management)
•	EJS – Experienced Job Seekers (Engineering & Non Engineering)
•	EAT – Employee Appraisal Testing (Private Sector Companies & SME’s)
•	PPS – Personality Profiling Soft-skill Assessment (General All)

                 
